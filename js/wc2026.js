// ── Flag rendering helper (uses flag-icons CSS library) ───────────────────
// Works on all platforms including Windows 10 where emoji flags show as "BR".
// Input: flag emoji ("🇧🇷") or 2-letter ISO code ("BR" or "br").
// Output: <span class="fi fi-br"></span>
function flagHtml(emojiOrCode, cls = '') {
  if (!emojiOrCode) return '';
  const extra = cls ? ` ${cls}` : '';
  // 2-letter code passed directly
  if (/^[A-Za-z]{2}$/.test(emojiOrCode)) {
    return `<span class="fi fi-${emojiOrCode.toLowerCase()}"${extra ? ` class="${extra.trim()}"` : ''}></span>`;
  }
  // Regional Indicator pair (most flags: 🇺🇸 🇧🇷 etc.)
  const pts = [...emojiOrCode].map(c => c.codePointAt(0));
  if (pts[0] >= 0x1F1E6 && pts[0] <= 0x1F1FF && pts.length >= 2) {
    const code = pts.slice(0, 2).map(p => String.fromCharCode(p - 0x1F1E6 + 97)).join('');
    return `<span class="fi fi-${code}"></span>`;
  }
  // Subdivision flags (England 🏴󠁧󠁢󠁥󠁮󠁧󠁿 etc.)
  const SUBDIVISIONS = { '🏴󠁧󠁢󠁥󠁮󠁧󠁿': 'gb-eng', '🏴󠁧󠁢󠁳󠁣󠁴󠁿': 'gb-sct', '🏴󠁧󠁢󠁷󠁬󠁳󠁿': 'gb-wls' };
  const sub = SUBDIVISIONS[emojiOrCode];
  if (sub) return `<span class="fi fi-${sub}"></span>`;
  // Fallback: just return the emoji as-is
  return emojiOrCode;
}

// Helper: build artilheiro custom picker (uses flag-icons — works on Windows)
// extraPlayers: optional array of { name, team } from Firestore settings/players_extra
function buildPlayerOptions(selectEl, lang, savedValue, extraPlayers = []) {
  const base = typeof WC2026_PLAYERS !== 'undefined' ? WC2026_PLAYERS : [];
  const all  = [...base, ...extraPlayers];

  // Build flat sorted player list — include players even if team isn't in ALL_TEAMS
  const seen = new Set();
  const players = [];
  all.forEach(p => {
    if (seen.has(p.name)) return;          // deduplicate
    seen.add(p.name);
    const team     = ALL_TEAMS.find(t => t.id === p.team);
    const teamName = team ? team.name : (p.team || '');
    const teamFlag = team ? team.flag : '';
    players.push({
      value:    p.name,
      name:     p.name,
      teamName,
      teamFlag,
      _sort: (teamName || 'zzz').toLowerCase() + p.name.toLowerCase(),
    });
  });
  players.sort((a, b) => a._sort.localeCompare(b._sort));

  // Populate the native select with real <option> elements so that
  // `selectEl.value = ...` actually sticks (a value with no matching option
  // is silently rejected by the browser → artilheiro saved as empty string).
  selectEl.innerHTML = '';
  const blankOpt = document.createElement('option');
  blankOpt.value = '';
  selectEl.appendChild(blankOpt);
  players.forEach(p => {
    const opt = document.createElement('option');
    opt.value = p.value;
    opt.textContent = p.name;
    selectEl.appendChild(opt);
  });

  // Hide original select (keep its ID so .value reads still work)
  selectEl.style.display = 'none';

  // Remove any existing picker
  const existingPicker = selectEl.parentNode.querySelector('.player-picker');
  if (existingPicker) existingPicker.remove();

  const ph    = '— Selecionar jogador —';
  const sph   = 'Buscar jogador ou seleção…';
  const noRes = 'Nenhum resultado';

  const wrapper = document.createElement('div');
  wrapper.className = 'player-picker';
  wrapper.innerHTML = `
    <button type="button" class="player-picker-trigger">
      <span class="player-picker-label">${ph}</span>
      <span class="player-picker-chevron">▾</span>
    </button>
    <div class="player-picker-panel">
      <div class="player-picker-search-wrap">
        <input type="text" class="player-picker-search" placeholder="${sph}" autocomplete="off">
      </div>
      <div class="player-picker-list"></div>
    </div>`;
  selectEl.parentNode.insertBefore(wrapper, selectEl.nextSibling);

  const trigger = wrapper.querySelector('.player-picker-trigger');
  const label   = wrapper.querySelector('.player-picker-label');
  const panel   = wrapper.querySelector('.player-picker-panel');
  const search  = wrapper.querySelector('.player-picker-search');
  const list    = wrapper.querySelector('.player-picker-list');

  function renderList(filter) {
    const q = (filter || '').toLowerCase();
    const hits = players.filter(p =>
      !q || p.name.toLowerCase().includes(q) || p.teamName.toLowerCase().includes(q)
    );
    list.innerHTML = '';
    if (!hits.length) {
      list.innerHTML = `<div class="player-picker-empty">${noRes}</div>`;
      return;
    }
    hits.forEach(p => {
      const item = document.createElement('div');
      item.className = 'player-picker-item' + (selectEl.value === p.value ? ' selected' : '');
      item.innerHTML = `${flagHtml(p.teamFlag)}<span class="player-picker-name">${p.name}</span><span class="player-picker-team">${p.teamName}</span>`;
      item.addEventListener('mousedown', e => {
        e.preventDefault();
        selectEl.value = p.value;
        label.innerHTML = `${flagHtml(p.teamFlag)} ${p.name}`;
        panel.classList.remove('open');
        trigger.classList.remove('open');
      });
      list.appendChild(item);
    });
  }

  trigger.addEventListener('click', () => {
    const open = panel.classList.toggle('open');
    trigger.classList.toggle('open', open);
    if (open) { search.value = ''; renderList(''); search.focus(); }
  });
  search.addEventListener('input', () => renderList(search.value));
  document.addEventListener('click', e => {
    if (!wrapper.contains(e.target)) {
      panel.classList.remove('open');
      trigger.classList.remove('open');
    }
  });

  // Restore saved value
  if (savedValue) {
    selectEl.value = savedValue;
    const p = players.find(pl => pl.value === savedValue);
    if (p) label.innerHTML = `${flagHtml(p.teamFlag)} ${p.name}`;
  }

  renderList('');
}
